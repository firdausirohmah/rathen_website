    let searchInputs = document.querySelectorAll(".field-autocomplete");

    searchInputs.forEach((searchInput) => {
        let input = searchInput.querySelector(".field-autocomplete input[type='text']");
        let inputHidden = searchInput.querySelector(".field-autocomplete input[type='hidden']");
        let liContainer = searchInput.querySelector(
            ".field-autocomplete .autocomplete-box"
        );

        input.addEventListener("keyup", function() {
            let inputValue = input.value;
            if (inputValue.length > 2) {

                var source = input.getAttribute('params') || 1;
                var route = "api-origin.json";
                if (source == 2) {
                    route = "api-destination.json";
                }
                $.ajax({
                    url: route,
                    data: {
                        "search": input.value
                    },
                    success: function(data) {

                        var result = data.data;
                        searchInput.classList.add("active");
                        let filteredWord = result.filter(function(word) {
                            return word.label.toLowerCase().includes(inputValue
                                .toLowerCase());
                        });

                        suggestionWordsGenerator(filteredWord);

                    }
                });
            } else {
                searchInput.classList.remove("active");
            }
        });

        let customListItem;

        function suggestionWordsGenerator(wordArray) {
            let suggestionWord = wordArray
                .map(function(word) {
                    return "<li data-id='" + word.code + "'>" + word.label + "</li>";
                })
                .join("");

            if (suggestionWord) {
                liContainer.innerHTML = suggestionWord;
            } else {
                liContainer.innerHTML = "<li>" + input.value + "</li>";
            }

            Select();
        }

        function Select() {
            let allListItems = liContainer.querySelectorAll("li");
            allListItems.forEach(function(wordItem) {
                wordItem.addEventListener("click", function(e) {
                    input.value = e.target.textContent;
                    inputHidden.value = e.target.dataset.id;
                    searchInput.classList.remove("active");
                });
            });
        }

        document.addEventListener('DOMContentLoaded', function() {
            var buttonSearchResi = document.querySelector('.button-search-resi');
            var btnHitung = document.querySelector('.btn-hitung');

            if (buttonSearchResi && btnHitung) {
                buttonSearchResi.addEventListener('click', function(event) {
                    btnHitung.click();
                });
            }
        });
    });
